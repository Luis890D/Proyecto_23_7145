    private void AbrirMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AbrirMenuActionPerformed
        PanelLateral.mostrar(Escritorio);

        // Obtener la referencia del panel de configuración que ya está visible
        PanelConfi panelConfiguracion = PanelLateral.getPanelConfi();

        if (panelConfiguracion != null) {
            Abrir gestorAbrir = new Abrir(Escritorio, this, panelConfiguracion);
            gestorAbrir.abrirArchivo();
        } else {
            JOptionPane.showMessageDialog(this, "Error: No se pudo acceder al panel de configuración");
        }
    }//GEN-LAST:event_AbrirMenuActionPerformed

