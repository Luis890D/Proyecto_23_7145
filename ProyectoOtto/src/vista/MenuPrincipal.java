    public void registrarHoja(JInternalFrame frame, JTextArea textArea, File archivo) {
        archivosPorFrame.put(frame, archivo);
        areasTextoPorFrame.put(frame, textArea);

        frame.addInternalFrameListener(new javax.swing.event.InternalFrameAdapter() {
            @Override
            public void internalFrameClosed(javax.swing.event.InternalFrameEvent evt) {
                archivosPorFrame.remove(frame);
                areasTextoPorFrame.remove(frame);
            }
        });

        frame.setTitle(archivo.getName());
        frame.setToolTipText(archivo.getAbsolutePath());
    }
